let to_file file =
  let process channel = 
    Construction.iter
      (fun o -> Printf.fprintf channel "%s\n" (o#def_langue_naturelle ())) in
  Output_fonctions.write_file file process

let to_string = 
  Output_fonctions.to_string (fun o -> o#def_langue_naturelle)
    
